                const specMatch = originalTitle.match(/(\d{4}p|4K|2160p|1080p|HDR|DV|æœæ¯”|é«˜ç |å†…å°|ç‰¹æ•ˆ|å­—å¹•|[\d\.]+G[B]?)/ig);
